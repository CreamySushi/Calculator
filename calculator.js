function ADD(a,b){
    return a+b
}
function SUBTRACT(a,b){
    return a-b
}
function MULTIPLY(a,b){
    return a*b
}
function DIVIDE(a,b){
    return a/b
}
function SQUARE(a,b){
    return a**b
}
let a = 10
let b = 12
console.log("ADD: ", ADD(a,b))
console.log("SUBTRACT: ", SUBTRACT(a,b))
console.log("MULTIPLY: ", MULTIPLY(a,b))
console.log("DIVIDE: ", DIVIDE(a,b))
console.log("SQUARE: ", SQUARE(a,b))
